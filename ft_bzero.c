#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*new_s;

	i = 0;
	new_s = (unsigned char *) s;
	while (i < n)
	{
		new_s[i] = 0;
		i++;
	}
}
